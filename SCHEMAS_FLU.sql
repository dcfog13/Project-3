	region_type VARCHAR(255) NOT NULL,
	region VARCHAR (255) NOT NULL,
	YEAR INT NOT NULL,
	WEEK INT NOT NULL,
	TOTAL_SPECIMENS VARCHAR (255),
	TOTAL_A VARCHAR (255),
	TOTAL_B VARCHAR (255),
	PERCENT_POSITVE VARCHAR (255),
	PERCENT_A VARCHAR (255),
	PERCENT_B VARCHAR (255)
	);
	SELECT * FROM CLINICAL
	
